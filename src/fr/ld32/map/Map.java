	public void render(double elapse, Shaders shader, Vec2 offset)
	{
		shader.setUniformMat4f("m_view", Mat4.translate(offset));
		shader.setUniform1f("anim", 0);
		texture.bind();
		if (mesh != null)
		{
			mesh.render(GL11.GL_QUADS);
		}
		Texture.unbind();
	}
	
